#include "libft.h"

int		ft_isspace(int c)
{
	return ((char)c == ' ' ||
			(char)c == '\t' ||
			(char)c == '\n' ||
			(char)c == '\v' ||
			(char)c == '\f' ||
			(char)c == '\r' ? 1 : 0);
}
