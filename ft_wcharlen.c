#include "libft.h"

size_t	ft_wcharlen(wint_t c)
{
	size_t	i;

	i = 1;
	if (c <= 0x7F)
		return (i);
	if (c > 0x1FFFFF)
		return (-1);
	while (1)
	{
		if (!(c >>= 6))
			break ;
		i++;
	}
	return (i);
}
