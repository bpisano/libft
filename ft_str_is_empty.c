#include "libft.h"

int		ft_str_is_empty(char *str)
{
	int		i;

	if (!str)
		return (0);
	i = -1;
	while (str[++i])
		if (str[i] != ' ' || str[i] != '\t')
			return (0);
	return (1);
}
