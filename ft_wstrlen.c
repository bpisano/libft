#include "libft.h"

size_t	ft_wstrlen(wchar_t *str)
{
	int		i;
	size_t	len;

	i = 0;
	len = 0;
	while (str[i])
	{
		len += ft_wcharlen(str[i]);
		i++;
	}
	return (len);
}
